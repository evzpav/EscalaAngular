package br.com.evandro.util;

public class ValidationError {
	
	
	private String msg;
	
	private boolean valid = true;

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public boolean isValid() {
		return valid;
	}

	public void setValid(boolean isValid) {
		this.valid = isValid;
	}

	
	
	
	
	
}
